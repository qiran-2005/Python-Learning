# Tuple methods

tuple1 = (1, 2, 3, 2, 4, 2, "Qiran")
count = tuple1.count(3)
print(count)

index = tuple1.index("Qiran")
print(index)

my_tuple = (1, 2, 3, 4, 5)
print(min(my_tuple))
print(len(my_tuple))

my_tuple1 = (1, 2, 3)
a, b, c = my_tuple1  # Unpacking the tuple
print(a, b, c)

print(a+b)
