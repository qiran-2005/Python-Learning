# LISTS

# Python lists are containers to store a set of values of any data type
# Lists are mutable, meaning their elements can be changed (modified, added, or removed) after creation.
# Lists are created using square brackets [].
# Due to their mutability, lists generally have more overhead and are slightly slower when compared to tuples.

friends = ["Qiran", "and", "Akshath", "are",
           "Friends", "from", 5, "Years", True]

print(friends[0:3])
