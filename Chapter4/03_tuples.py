# TUPLES

# Tuples are immutable, meaning once they are created, their elements cannot be changed, added, or removed.
# Tuples are created using parentheses () or simply commas (in the case of a single item, you need a trailing comma).
# Tuples are faster than lists because they are immutable, which means less memory is used, and fewer operations are needed to maintain them.

a = (1, "Qiran", True)
print(a)
print(type(a))

# Single element tuple
b = ("VS Code",)
print(type(b))
