# Loops

# Python has two main types of loops: for loop, which iterates over a sequence (like a list or range), and while loop, which continues as long as a given condition is true.

# Loop control statements such as break (to exit a loop), continue (to skip the current iteration), and pass (to do nothing) help control the flow of loops.

# Nested loops allow you to place one loop inside another, useful for iterating over multi-dimensional data like lists of lists.

print(1)
print(2)
print(3)
print(4)
print(5)

# There is a more efficient way to do this and it is using a Loop
print("For Loop")
for i in range(1, 6):
    print(i)
