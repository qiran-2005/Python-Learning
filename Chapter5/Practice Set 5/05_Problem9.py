# Problem 9
# Can you change the values inside a list which is contained in set S?
# s = {8, 7, 12, "Harry", [1,2]}


# No, you cannot change the values inside a list that is contained in a set in Python, because lists are mutable and sets require their elements to be immutable. This means that you cannot directly add a list (or any other mutable object) to a set.
